    nome VARCHAR(100) NOT NULL,
    datadenascimento DATE,
    idade INT NOT NULL,
    casaH VARCHAR(100) NOT NULL,
    habilidadeE VARCHAR(100) NOT NULL,
    sangue VARCHAR(100) NOT NULL,
);

